 function showEnglish() {
  document.getElementById("englishText").style.display = "inline";
  document.getElementById("hindiText").style.display = "none";
  document.getElementById("marathiText").style.display = "none";
}
    
function showHindi() {
  document.getElementById("englishText").style.display = "none";
  document.getElementById("hindiText").style.display = "inline";
  document.getElementById("marathiText").style.display = "none";
}

 function showMarathi() {
  document.getElementById("englishText").style.display = "none";
  document.getElementById("hindiText").style.display = "none";
  document.getElementById("marathiText").style.display = "inline";
}
